def write_report(output_dir: str, dives, results, summaries, stats) -> None:
    os.makedirs(output_dir, exist_ok=True)

    with open(os.path.join(output_dir, "index.html"), "w", encoding="utf-8") as fh:
        fh.write(render_index(summaries, stats))

    for dive, result, summary in zip(dives, results, summaries):
        page = render_dive(dive, result, summary)
        with open(os.path.join(output_dir, f"dive_{dive.number}.html"),
                  "w", encoding="utf-8") as fh:
            fh.write(page)

    with open(os.path.join(output_dir, "statistics.json"), "w", encoding="utf-8") as fh:
        json.dump(stats, fh, indent=2)
