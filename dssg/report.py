def _svg_depth_profile(samples, max_depth: float, duration_s: float,
                       width: int = 640, height: int = 240) -> str:
    pad_l, pad_r, pad_t, pad_b = 44, 14, 14, 28
    w = width - pad_l - pad_r
    h = height - pad_t - pad_b
    dur = max(duration_s, 1.0)
    dmax = max(max_depth, 1.0)

    def x(t):
        return pad_l + (t / dur) * w

    def y(d):
        return pad_t + (d / dmax) * h

    pts = " ".join(f"{x(s.time_s):.1f},{y(s.depth_m):.1f}" for s in samples)
    area = f"{x(0):.1f},{y(0):.1f} " + pts + f" {x(samples[-1].time_s):.1f},{y(0):.1f}"

    grid = []
    for frac in (0.25, 0.5, 0.75, 1.0):
        yy = pad_t + frac * h
        grid.append(f"<line x1='{pad_l}' y1='{yy:.0f}' x2='{pad_l + w}' y2='{yy:.0f}' "
                    f"stroke='#2a3a5c' stroke-width='1'/>")
        grid.append(f"<text x='{pad_l - 6}' y='{yy + 4:.0f}' fill='#9fb0c8' "
                    f"font-size='10' text-anchor='end'>{dmax * frac:.0f}m</text>")
    for frac in (0.0, 0.5, 1.0):
        xx = pad_l + frac * w
        grid.append(f"<text x='{xx:.0f}' y='{height - 8}' fill='#9fb0c8' "
                    f"font-size='10' text-anchor='middle'>{dur / 60 * frac:.0f}min</text>")

    return (
        f"<svg viewBox='0 0 {width} {height}' role='img' aria-label='Depth profile'>"
        f"{''.join(grid)}"
        f"<polygon points='{area}' fill='#38bdf833'/>"
        f"<polyline points='{pts}' fill='none' stroke='#38bdf8' stroke-width='2'/>"
        f"</svg>"
    )


def _svg_compartment_bars(gradients: list[float], leading: int,
                          width: int = 640, height: int = 260) -> str:
    pad_l, pad_r, pad_t, pad_b = 40, 14, 14, 40
    w = width - pad_l - pad_r
    h = height - pad_t - pad_b
    n = len(gradients)
    gmax = max(max(gradients), 0.01)
    gmin = min(min(gradients), 0.0)
    span = gmax - gmin or 1.0
    bw = w / n * 0.7
    gap = w / n

    def y(v):
        return pad_t + (gmax - v) / span * h

    zero_y = y(0.0)
    bars = [f"<line x1='{pad_l}' y1='{zero_y:.1f}' x2='{pad_l + w}' y2='{zero_y:.1f}' "
            f"stroke='#9fb0c8' stroke-width='1' stroke-dasharray='3 3'/>"]
    for i, g in enumerate(gradients):
        cx = pad_l + i * gap + (gap - bw) / 2
        top = y(max(g, 0.0))
        bottom = y(min(g, 0.0))
        colour = "#f472b6" if (i + 1) == leading else ("#34d399" if g > 0 else "#9fb0c8")
        bars.append(
            f"<rect x='{cx:.1f}' y='{top:.1f}' width='{bw:.1f}' "
            f"height='{max(bottom - top, 1):.1f}' fill='{colour}' rx='2'>"
            f"<title>Compartment {i + 1} ({N2_HALFTIMES[i]:.1f} min N2): "
            f"{g * 10:.2f} msw</title></rect>"
        )
        bars.append(f"<text x='{cx + bw / 2:.1f}' y='{height - 24}' fill='#9fb0c8' "
                    f"font-size='9' text-anchor='middle'>{i + 1}</text>")
    bars.append(f"<text x='{pad_l + w / 2:.0f}' y='{height - 6}' fill='#9fb0c8' "
                f"font-size='11' text-anchor='middle'>Tissue compartment "
                f"(fast &rarr; slow)</text>")
    return (
        f"<svg viewBox='0 0 {width} {height}' role='img' "
        f"aria-label='Per-compartment surface supersaturation gradient'>"
        f"{''.join(bars)}</svg>"
    )


def _svg_histogram(hist: dict, width: int = 640, height: int = 220,
                   unit_label: str = "DSSG (bar/ata)") -> str:
    edges = hist.get("edges", [])
    counts = hist.get("counts", [])
    if not counts:
        return "<p class='muted'>Not enough data for a histogram.</p>"
    pad_l, pad_r, pad_t, pad_b = 36, 14, 14, 36
    w = width - pad_l - pad_r
    h = height - pad_t - pad_b
    n = len(counts)
    cmax = max(counts) or 1
    bw = w / n * 0.8
    gap = w / n
    bars = []
    for i, c in enumerate(counts):
        bh = (c / cmax) * h
        cx = pad_l + i * gap + (gap - bw) / 2
        cy = pad_t + h - bh
        bars.append(f"<rect x='{cx:.1f}' y='{cy:.1f}' width='{bw:.1f}' "
                    f"height='{bh:.1f}' fill='#38bdf8' rx='2'><title>"
                    f"{edges[i]:.1f}–{edges[i + 1]:.1f} msw: {c} dives</title></rect>")
        bars.append(f"<text x='{cx + bw / 2:.1f}' y='{cy - 4:.1f}' fill='#e8eef7' "
                    f"font-size='10' text-anchor='middle'>{c if c else ''}</text>")
    for i in (0, n // 2, n):
        xx = pad_l + i * gap
        val = edges[i] if i < len(edges) else edges[-1]
        bars.append(f"<text x='{xx:.0f}' y='{height - 6}' fill='#9fb0c8' "
                    f"font-size='10' text-anchor='middle'>{val:.1f}</text>")
    bars.append(f"<text x='{pad_l + w / 2:.0f}' y='{height - 20}' fill='#9fb0c8' "
                f"font-size='11' text-anchor='middle'>{unit_label}</text>")
    return (f"<svg viewBox='0 0 {width} {height}' role='img' "
            f"aria-label='DSSG distribution'>{''.join(bars)}</svg>")


