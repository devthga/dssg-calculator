SORT_JS = """
function sortTable(table, col, numeric) {
  const tbody = table.tBodies[0];
  const rows = Array.from(tbody.rows);
  const dir = table.getAttribute('data-dir-' + col) === 'asc' ? -1 : 1;
  table.setAttribute('data-dir-' + col, dir === 1 ? 'asc' : 'desc');
  rows.sort((a, b) => {
    let x = a.cells[col].getAttribute('data-v') ?? a.cells[col].innerText;
    let y = b.cells[col].getAttribute('data-v') ?? b.cells[col].innerText;
    if (numeric) { x = parseFloat(x) || 0; y = parseFloat(y) || 0; return (x - y) * dir; }
    return x.localeCompare(y) * dir;
  });
  rows.forEach(r => tbody.appendChild(r));
}
document.querySelectorAll('table.sortable').forEach(t => {
  Array.from(t.tHead.rows[0].cells).forEach((th, i) => {
    th.addEventListener('click', () => sortTable(t, i, th.classList.contains('num')));
  });
});
"""


def _esc(value) -> str:
    return html.escape(str(value)) if value is not None else ""


def _page(title: str, body: str, extra_js: str = "") -> str:
    js = f"<script>{SORT_JS}{extra_js}</script>" if (SORT_JS or extra_js) else ""
    return (
        "<!DOCTYPE html><html lang='en'><head><meta charset='utf-8'>"
        "<meta name='viewport' content='width=device-width, initial-scale=1'>"
        f"<title>{_esc(title)}</title><style>{CSS}</style></head>"
        f"<body><div class='wrap'>{body}"
        "<footer>Generated by the MacDive DSSG calculator &middot; "
        "DSSG = DAN Surface Supersaturation Gradient (Buhlmann ZH-L16)</footer>"
        f"</div>{js}</body></html>"
    )


